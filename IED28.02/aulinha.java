public class aulinha {
    public static void main(String[] args) {
        boolean flag = true;
        char ch = 'a';
        byte algo = 12;
        short s = 24;
        int i = 123456;
        float j  = 3.14159F;
        double d = 21828;
        long a = 814L;
        
        System.out.println("Variável flag: " + flag);
        System.out.println("Variável char: " + ch);
        System.out.println("Variável byte: " + algo);
        System.out.println("Variável short: " + s);
        System.out.println("Variável int: " + i);
        System.out.println("Variável float: " + j);
        System.out.println("Variável double: " + d);
        System.out.println("Variável long: " + a);
    }
}